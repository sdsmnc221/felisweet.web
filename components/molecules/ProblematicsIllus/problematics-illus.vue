          repeatRefresh: true,
          yoyo: true,
          duration: 2.4,
          ease: 'circ.in',
        },
        'start'
      )
      .to(
        this.$refs.cat.$el,
        {
          y: 'random(0, -240px)',
          x: 'random(-120px, 0)',
          repeat: -1,
          repeatRefresh: true,
          yoyo: true,
          duration: 0.6,
          ease: 'circ.in',
        },
        'start'
      )

    window.requestAnimationFrame(() => {
      this.blinking()
    })
  },
  methods: {
    blinking() {
      let eyes = this.$gsap.from(
        [...this.$refs.illus.querySelectorAll('[id^=Eyes]')],
        0.2,
        {
          scaleY: 'random(0.4, 0.8)',
          y: 'random(-4.8, 4.8)',
          x: 'random(-4.8, 4.8)',
          transformOrigin: 'center top',
        }
      )

      const tl = this.$gsap.timeline({
        paused: true,
        repeat: 1,
        yoyo: true,
        onCompleteParams: ['{self}'],
        onComplete: () => {
          tl.remove(eyes)
          eyes = this.$gsap.from(
            [...this.$refs.illus.querySelectorAll('[id^=Eyes]')],
            0.2,
            {
              scaleY: 'random(0.4, 0.8)',
              y: 'random(-4.8, 4.8)',
              x: 'random(-4.8, 4.8)',
              transformOrigin: 'center top',
            }
          )
          tl.add(eyes)
          this.$gsap.delayedCall(3 * Math.random() + 0.6, tl.restart, [], tl)
        },
      })

      tl.add(eyes)
      tl.progress(1).progress(0)
      tl.play()

      return tl
    },
  },
}
</script>

